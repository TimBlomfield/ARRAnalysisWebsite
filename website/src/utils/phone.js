export const getCountryCodeFromNumber = num1 => {
  let num = num1.replace(/\s+/g, '').substring(1); // Remove all spaces and the plus

  for (let lenOne = 1; lenOne < 4; ++lenOne) {
    const ccx = num.substring(0, lenOne);
    if (ccx.length === lenOne) {
      const potential = arrPhoneShortcuts[ccx];
      if (potential != null) {
        for (let lenTwo = 1; lenTwo < 5; ++lenTwo) {
          const ccy = num.substring(lenOne, lenOne + lenTwo);
          if (ccy.length === lenTwo) {
            if (potential[ccy] != null) return potential[ccy];
          } else break;
        }
        return potential.main;
      }
    } else break;
  }

  return null;
};
